window.applyTranslations = function (lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else if (el.tagName === 'SELECT') {
                // Special handling for first option placeholder
                if (el.options[0] && el.options[0].value === "") {
                    el.options[0].text = translations[lang][key];
                }
            } else {
                el.innerText = translations[lang][key];
            }
        }
    });

    // Update label text if specified in the button
    const currentLangLabel = document.getElementById('current-lang');
    if (currentLangLabel) {
        const langNames = { de: 'Deutsch', en: 'English', fr: 'Français', es: 'Español' };
        currentLangLabel.innerText = langNames[lang];
    }
};

window.selectLang = function (label, code) {
    applyTranslations(code);
    const langMenu = document.getElementById('lang-menu');
    if (langMenu) langMenu.classList.add('hidden');
    localStorage.setItem('selectedLang', code);
    document.documentElement.lang = code;
    console.log(`Language changed to: ${label} (${code})`);
};

// Detectar idioma del navegador
function detectBrowserLanguage() {
    const browserLang = navigator.language || navigator.userLanguage;
    const langCode = browserLang.split('-')[0].toLowerCase();
    const supportedLangs = ['de', 'en', 'fr', 'es'];
    
    if (supportedLangs.includes(langCode)) {
        return langCode;
    }
    return 'de'; // Idioma por defecto
}

document.addEventListener('DOMContentLoaded', () => {
    // Primero verificar si hay un idioma guardado, sino detectar del navegador
    const savedLang = localStorage.getItem('selectedLang');
    const langToUse = savedLang || detectBrowserLanguage();
    
    // Guardar el idioma detectado si no hay uno guardado
    if (!savedLang) {
        localStorage.setItem('selectedLang', langToUse);
    }
    
    document.documentElement.lang = langToUse;
    applyTranslations(langToUse);
    
    console.log(`Language detected/set to: ${langToUse}`);
});
