// Language names mapping
const languageNames = {
    de: "Deutsch",
    en: "English",
    fr: "Français",
    es: "Español"
};

// Function to get URL parameter
function getUrlParameter(name) {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get(name);
}

// Function to apply translations to the page
function applyTranslations(lang) {
    const elements = document.querySelectorAll('[data-i18n]');

    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Also update the HTML lang attribute
    document.documentElement.lang = lang;
}

// Function to select and apply language
function selectLang(langName, langCode) {
    // Update URL
    const url = new URL(window.location.href);
    url.searchParams.set('lang', langCode);
    window.history.pushState({}, '', url);

    // Update current language display
    const currentLangElement = document.getElementById('current-lang');
    if (currentLangElement) {
        currentLangElement.textContent = langName;
    }

    // Apply translations
    applyTranslations(langCode);

    // Save preference
    localStorage.setItem('preferredLanguage', langCode);

    // Hide menu
    const langMenu = document.getElementById('lang-menu');
    if (langMenu) {
        langMenu.classList.add('hidden');
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function () {
    // Check URL parameter first, then localStorage, then default to German
    const urlLang = getUrlParameter('lang');
    const savedLang = localStorage.getItem('preferredLanguage');
    const browserLang = navigator.language.split('-')[0];

    let lang = 'de'; // Default to German
    let langName = 'Deutsch';

    if (urlLang && translations[urlLang]) {
        lang = urlLang;
        langName = languageNames[urlLang];
    } else if (savedLang && translations[savedLang]) {
        lang = savedLang;
        langName = languageNames[savedLang];
    } else if (translations[browserLang]) {
        lang = browserLang;
        langName = languageNames[browserLang];
    }

    // Apply the language
    selectLang(langName, lang);
});
